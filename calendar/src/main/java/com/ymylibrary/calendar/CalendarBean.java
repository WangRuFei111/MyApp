package com.ymylibrary.calendar;

/**
 * 原点的日期
 */
public class CalendarBean {

    private int Dotyear, Dotmonth, Dotday;

    public CalendarBean() {

    }

    public int getDotyear() {
        return Dotyear;
    }

    public void setDotyear(int dotyear) {
        Dotyear = dotyear;
    }

    public int getDotmonth() {
        return Dotmonth;
    }

    public void setDotmonth(int dotmonth) {
        Dotmonth = dotmonth;
    }

    public int getDotday() {
        return Dotday;
    }

    public void setDotday(int dotday) {
        Dotday = dotday;
    }
}
